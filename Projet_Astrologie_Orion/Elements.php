                        mettre beaucoup de sentiment et d'émotions dans vos joutes amoureuses même si vous manquez parfois de 
                        combativité ou si, au contraire, vous vous montrez jaloux et possessif, souvent par manque de confiance 
                        en vos charmes ou en vos moyens. C'est dans une relation durable, bâtie sur la confiance mutuelle que 
