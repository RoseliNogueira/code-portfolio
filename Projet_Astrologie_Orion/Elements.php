                    <h3 id="Eau">L'élément Eau</h3>
                    <p>L'<em>eau</em> est le quatrième élément, négatif, féminin et introverti. Il caractérise les signes 
                        du Cancer, du Scorpionet du Poissons.</p>
                    <p>L'eau entraîne l'air logique et rationnel vers l'univers du ressenti, des émotions, des sensations et 
                        de l'instinct. Vous vous distinguez par votre sensibilité, un certain conformisme, votre intuition et 
                        votre désir sincère d'aider les autres. Vous êtes sentimental, rêveur, souvent idéaliste et votre 
                        réceptivité est disposée à écouter et comprendre chacun autour de vous. Vous êtes très sensible aux 
                        ambiances et vous participez émotivement à tout ce qui se passe dans votre environnement. Mais votre 
                        gentillesse instinctive se teinte parfois d'humeurs bizarres ou d'explosions de susceptibilité. Il vous 
                        faut alors prendre du recul, vous isoler pour vous protéger de la dureté du monde.</p>
