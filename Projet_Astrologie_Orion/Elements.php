                    <h3 id="Feu">L'élément Feu</h3>
                    <p>Le <em>feu</em> est le premier élément, positif, masculin et extraverti. Il caractérise les 
                        signes du Bélier, du Lion et du Sagittaire.</p>
                    <p>On dit souvent des signes de feu qu'ils ont un sacré caractère ou un sacré tempérament. 
                        Vous vous caractérisez en premier lieu par la fougue, l'enthousiasme, la volonté, le désir
                        de réussir et le goût de la conquête. Vous ne comptez généralement que sur vous-même pour 
                        réussir votre vie, vous avez confiance en vous et en la vie, vous aspirez à votre autonomie, 
                        vous avez des ambitions ou des idéaux élevés et une capacité de vision à long terme.</p>
                    <p>Mais vous êtes souvent impatient, égoïste ou intolérant. Vous manquez parfois de recul ou d'objectivité
                        dans vos opinions, et vous avez tendance à vous montrer autoritaire sous couvert de bienveillance.</p>
                    <h3>En Amour</h3>
                    <p>Les signes de feu sont spontanés et réactifs, vous appréciez les expériences et les sensations fortes 
                        en amour. Vous aimez en général passionnément, sans toujours réfléchir et avec beaucoup d'exubérance. 
                        Vous redoutez l'ennui et pimentez vos relations de défis sulfureux ou de challenges hasardeux, quand ce
                        n'est pas une touche d'exotisme. Vous faites en général beaucoup d'efforts pour impressionner votre 
                        partenaire ou le surprendre mais ce désir de vous mettre en avant peut paradoxalement vous faire passer
                        pour égocentrique, mettez de la modération dans l'expression de vos sentiments. Pourvu que la routine ne 
                        s'installe pas, les signes influencés par le feu apprécient les belles histoires d'amour qui finissent bien.</p>
