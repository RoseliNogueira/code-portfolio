                    <h3 id="Terre">L'élément Terre</h3>
                    <p>La <em>terre</em> est le deuxième élément, négatif, féminin et introverti. Il caractérise les signes du Taureau,
                        de la Vierge et du Capricorne.</p>
                    <p>Dans la symbolique, la terre suit le feu. Elle concrétise les conquêtes du feu, les rendant durables, aménageables
                        en matière et en formes. Vous avez un tempérament calme, réaliste, parfois matérialiste, rigoureux et laborieux. 
                        Vous construisez des choses concrètes ou vous alimentez de manière pratique votre sécurité matérielle. Votre perception
                        du réel est juste et vous permet de bons investissements, des objectifs rentables, une belle chance de réussite matérielle.
                        Mais vous êtes souvent froid ou détaché et vous manquez de souplesse, de légèreté, de fantaisie, de tolérance. Votre côté
                        trop réaliste fait de vous un Saint Thomas qui ne croit que ce qu'il voit.</p>                    
                    <h3>En Amour</h3>
                    <p>Les besoins amoureux des signes de terre sont souvent primaires : vous recherchez avant tout une grande sensualité mais
                        aussi la sécurité, la stabilité et la tranquillité. Vous appréciez la vie de famille et cherchez à fonder votre foyer
                        relativement jeune. Mais vous prenez votre temps pour vous décider car vous tenez à ancrer vos relations dans la durée
                        avec un minimum de risques, de surprises, de changements. Votre besoin de sécurité exacerbé peut cependant inquiéter 
                        votre partenaire qui risque de vous trouver possessif ou étouffant. Vous êtes à la recherche d'une valeur sûre et vous
                        n'êtes pas insensible à la beauté physique.</p>
