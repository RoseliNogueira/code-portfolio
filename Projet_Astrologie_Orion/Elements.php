                    <h3 id="Air">L'élément Air</h3>
                    <p>L' <em>air</em> est le troisième élément, positif, masculin et extraverti. Il caractérise les signes du Gémeaux, de la 
                        Balance et du Verseau.</p>
                    <p>Après que la terre ait donné des formes concrètes à l'énergie du feu, voici l'esprit donné aux choses : les signes d'air
                        symbolisent l'intellect, les échanges avec autrui, les arts. Vous avez la capacité de vous adapter à tout ce qui vous 
                        entoure, vous êtes sociable avec un intérêt marqué pour tout ce qui est humain. Vous cherchez à vous épanouir au travers
                        de vos contacts avec les autres. Vous vous distinguez par vos capacités intellectuelles et votre besoin de communiquer.</p>
                    <p>Mais vous avez des côtés instables et indécis, vous avez tendance à vous disperser ou à survoler les situations. Vous êtes
                        trop cérébral, superficiel ou critique...</p>
                    <h3>En Amour</h3>
                    <p>Trop cérébral vous avez parfois du mal à laisser place au désir quand il se présente. Vous élaborez des raisonnements qui 
                        nuisent parfois à votre lucidité et vous manquez des occasions de jouir d'une opportunité sensuelle à force de décortiquer 
                        vos motivations ou de vous interroger sur votre attirance. Vous avez du mal à exprimer vos émotions mais vous avez envie de 
                        vous engager et aussi d'apprendre dans le domaine des relations amoureuses. Votre instabilité ou votre indécision peut 
                        cependant fatiguer un partenaire plus simple ou plus naturel ou plus instinctif.</p>
